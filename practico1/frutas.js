// a. Crea un arreglo vacío llamado frutas
let frutas = [];

// b. Agrega cinco frutas distintas al arreglo
frutas.push("manzana", "plátano", "naranja", "uva", "mango");

// c. Obtén la última fruta agregada
let ultima = frutas[frutas.length - 1];

// Imprime la última fruta por consola
console.log("La última fruta agregada fue: " + ultima);

// d. Obtén la primera fruta agregada
let primera = frutas[0];

// Imprime la primera fruta por consola
console.log("La primera fruta agregada fue: " + primera);